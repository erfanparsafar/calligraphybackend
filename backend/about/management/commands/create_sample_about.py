from django.core.management.base import BaseCommand
from about.models import AboutPage

class Command(BaseCommand):
    help = 'ایجاد نمونه داده برای صفحه درباره استاد'

    def handle(self, *args, **options):
        if not AboutPage.objects.exists():
            AboutPage.objects.create(
                title='درباره استاد اسماعیلی مود',
                content='''استاد اسماعیلی مود یکی از برجسته‌ترین اساتید خوشنویسی ایران است که بیش از ۲۰ سال تجربه در آموزش این هنر اصیل ایرانی دارد.

وی از سنین کودکی به هنر خوشنویسی علاقه‌مند بوده و زیر نظر اساتید برجسته‌ای همچون استاد غلامحسین امیرخانی و استاد عباس اخوین آموزش دیده است.

استاد اسماعیلی مود در طول سال‌های فعالیت خود، صدها هنرجو را در سطوح مختلف آموزش داده و بسیاری از آن‌ها امروز از اساتید برجسته خوشنویسی هستند.

وی علاوه بر تدریس، در زمینه تحقیق و پژوهش در حوزه خوشنویسی نیز فعالیت داشته و مقالات متعددی در این زمینه منتشر کرده است.

استاد اسماعیلی مود معتقد است که خوشنویسی نه تنها یک هنر زیبا، بلکه راهی برای تقویت صبر، دقت و تمرکز است که می‌تواند در تمام جنبه‌های زندگی مفید باشد.''',
                is_active=True
            )
            self.stdout.write(
                self.style.SUCCESS('نمونه داده برای صفحه درباره استاد با موفقیت ایجاد شد.')
            )
        else:
            self.stdout.write(
                self.style.WARNING('داده‌ای برای صفحه درباره استاد از قبل وجود دارد.')
            ) 